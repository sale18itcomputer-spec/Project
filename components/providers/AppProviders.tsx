// User-picked accent color, stored as the H/S components of --primary's
// "H S% L%" triple — L stays theme-driven so contrast keeps working across
// every theme. null means "use the active theme's default accent".
export type AccentColor = { h: number; s: number } | null;

const ThemeContext = React.createContext<{
    theme: ThemeMode;
    isDark: boolean;
    setTheme: (theme: ThemeMode) => void;
    accent: AccentColor;
    setAccent: (accent: AccentColor) => void;
}>({ theme: 'light', isDark: false, setTheme: () => {}, accent: null, setAccent: () => {} });

export const useTheme = () => React.useContext(ThemeContext);

// Re-registers the ECharts theme (which reads --primary etc.) and forces all
// live chart instances to redraw. Needed after either the theme class or the
// accent CSS variables change.
function reapplyChartTheme() {
    import('echarts').then(echarts => {
        import('../charts/echartsTheme').then(({ buildLimperialTheme }) => {
            echarts.registerTheme('limperial', buildLimperialTheme());
            // Trigger resize on all live ECharts instances so they re-apply
            // the new theme. A resize call causes ECharts to re-render fully.
            try {
                (echarts as any).getInstanceByDom &&
                document.querySelectorAll('[_echarts_instance_]').forEach((el) => {
                    const instance = (echarts as any).getInstanceByDom(el as HTMLElement);
                    if (instance && !instance.isDisposed()) {
                        instance.resize();
                    }
                });
            } catch (_e) {
                // Silently ignore — some chart instances may not exist yet
            }
        });
    });
}

function ThemeProvider({ children }: { children: React.ReactNode }) {
    const [theme, setThemeState] = React.useState<ThemeMode>(() => {
        if (typeof window === 'undefined') return 'light';
        const saved = localStorage.getItem('limperial-theme');
        if (saved && THEME_META.some(t => t.id === saved)) return saved as ThemeMode;
        return window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light';
    });

    React.useEffect(() => {
        const root = document.documentElement;
        const meta = THEME_META.find(t => t.id === theme);

        if (meta?.dark) {
            root.classList.add('dark');
            root.classList.remove('b2b-dark');
        } else {
            root.classList.remove('dark');
        }

        root.classList.remove('claude');
        NEW_THEME_IDS.forEach(id => root.classList.remove(`theme-${id}`));

        if (theme === 'claude') {
            root.classList.add('claude');
        } else if (NEW_THEME_IDS.includes(theme)) {
            root.classList.add(`theme-${theme}`);
        }

        const bg = THEME_BACKGROUNDS[theme];
        if (bg.effectColor) {
            root.style.setProperty('--bg-effect-color', bg.effectColor);
        } else {
            root.style.removeProperty('--bg-effect-color');
        }
        root.style.setProperty('--bg-effect-intensity', String(bg.intensity ?? 1));

        localStorage.setItem('limperial-theme', theme);

        reapplyChartTheme();
    }, [theme]);

    const [accent, setAccentState] = React.useState<AccentColor>(() => {
        if (typeof window === 'undefined') return null;
        try {
            const saved = localStorage.getItem('limperial-accent');
            if (!saved) return null;
            const parsed = JSON.parse(saved);
            if (typeof parsed?.h === 'number' && typeof parsed?.s === 'number') return parsed;
        } catch (_e) {
            // Ignore malformed localStorage value
        }
        return null;
    });

    React.useEffect(() => {
        const root = document.documentElement;
        if (accent) {
            root.style.setProperty('--accent-h', String(accent.h));
            root.style.setProperty('--accent-s', `${accent.s}%`);
            localStorage.setItem('limperial-accent', JSON.stringify(accent));
        } else {
            root.style.removeProperty('--accent-h');
            root.style.removeProperty('--accent-s');
            localStorage.removeItem('limperial-accent');
        }

        reapplyChartTheme();
    }, [accent]);

    const setAccent = React.useCallback((next: AccentColor) => setAccentState(next), []);

    const setTheme = React.useCallback((next: ThemeMode) => setThemeState(next), []);

    const isDark = THEME_META.find(t => t.id === theme)?.dark ?? false;

    return (
        <ThemeContext.Provider value={{ theme, isDark, setTheme, accent, setAccent }}>
            <ThemeBackground />
            {children}
        </ThemeContext.Provider>
    );
}
