// Theme ids that map to a dedicated `.theme-<id>` CSS class (everything
// beyond the original light/dark/claude trio, which use bare :root/.dark/.claude).
const NEW_THEME_IDS: ThemeMode[] = THEME_META.map(t => t.id).filter(id => id !== 'light' && id !== 'dark' && id !== 'claude');

// Ambient animated-background patterns, ported from odysseus. `effectColor`
// overrides the default (the theme's --foreground), `intensity` scales
// particle density/opacity (0..1, default 1).
export type BgPattern =
    | 'none' | 'dots' | 'synapse' | 'rain' | 'constellations'
    | 'perlin-flow' | 'petals' | 'sparkles' | 'embers';

export type ThemeBackground = {
    pattern: BgPattern;
    effectColor?: string;
    intensity?: number;
};

